    public void generarGrafica(String polinomio, double limX0, double limX1) throws NumberFormatException {
        String def = polinomio;
        double r;
        double lx0 = limX0;
        double lx1 = limX1;
        //double le=Double.parseDouble(def)
        //int ln= Integer.parseInt()
        lx0 = 0;
        lx1 = 0;

        funcion ff = new funcion(def);

        metodo s = new metodo();

        r = s.raiz(ff, lx0, lx1, 100, 1e-6);

        //jTextField_Resultado.setText("" + r);
        double[] x = new double[200];
        double[] y = new double[200];
        double xi = lx0 - 10;
        for (int i = 0; i < 200; i++) {
            x[i] = xi + i * 0.1;

            y[i] = ff.evaluar(x[i]);

        }

        grafica.addLegend("SOUTH");
        grafica.removeAllPlots();
        grafica.addLinePlot(def, x, y);
    }

   

